// Traffic Light System

let color = 'green'; // Change this value to "red", "yellow", or "green"

if (color === 'red') {
    console.log('STOP!');
} else if (color === 'yellow') {
    console.log('Go Slow');
} else if (color === 'green') {
    console.log('GO!');
} else {
    console.log('Invalid color! Please enter red, yellow, or green.');
}

// Show all possible signals at the end
console.log('Traffic Light Rules:');
console.log('Red -> STOP');
console.log('Yellow -> Go Slow');
console.log('Green -> GO');
